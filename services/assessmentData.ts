function buildAssessments(lang: Lang): Record<string, AssessmentConfig> {
  const opts = agreementOptionsMap[lang];

  const buildQuiz = (id: string, icon: string, gradient: string): AssessmentConfig => {
    const meta = metaTranslations[id][lang];
    const questions = (questionsDatabase[id] || []).map(q => ({
      id: q.id,
      text: lang === 'kk' && questionTranslations[q.id] ? questionTranslations[q.id] : q.text,
      category: categoryMap[q.category]?.[lang] || q.category,
      options: opts,
    }));
    return { id, type: 'quiz', title: meta.title, description: meta.description, icon, gradient, questions };
  };

  const buildChat = (id: string, icon: string, gradient: string): AssessmentConfig => {
    const meta = metaTranslations[id][lang];
    return {
      id,
      type: 'chat',
      title: meta.title,
      description: meta.description,
      icon,
      gradient,
      initialMessage: meta.initialMessage,
      systemInstruction: meta.systemInstruction,
    };
  };

  return {
    'career-riasec': buildQuiz('career-riasec', 'school', 'from-blue-600 to-indigo-700'),
    'personality-big5': buildQuiz('personality-big5', 'psychology_alt', 'from-purple-600 to-fuchsia-700'),
    'soft-skills-360': buildChat('soft-skills-360', 'handshake', 'from-slate-600 to-slate-800'),
    'health-stress': buildChat('health-stress', 'battery_alert', 'from-orange-500 to-red-600'),
    'values-motivation': buildChat('values-motivation', 'diamond', 'from-emerald-600 to-teal-700'),
    'emotional-intelligence': buildQuiz('emotional-intelligence', 'favorite', 'from-rose-500 to-pink-600'),
    'learning-style': buildChat('learning-style', 'school', 'from-cyan-500 to-blue-600'),
  };
}

export const getAssessmentById = (id: string, lang: Lang = 'ru'): AssessmentConfig | undefined => {
  return buildAssessments(lang)[id];
};

export const getAllAssessments = (lang: Lang = 'ru') => Object.values(buildAssessments(lang));
